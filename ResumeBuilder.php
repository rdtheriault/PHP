NOTE - This is a good simple formatter for your resume based on years of HR recruiting experience, it is not an end all solution and is meant as a starting point. The goal is to produce a resume<br>
that you can copy into websites that strip all formatting (the formatting will work well on all sites I have seen). But there will also be instruction on how to format the resume to make it present better.<br>
The layout is in the general final layout for visualization purposes (i.e. it's current design looks mostly like it will at the completion).<br>
<br>
This resume builder will output a text file that you can open in a document editor and modify or use to copy to an online application, as stated before.<br>
For the most part, items that are left blank will not show up on the final product (except as noted below). If there are not enough sections in any category, you can run<br>
this site one more time only adding to the categories that you need. Then just copy that data into your first text file. Again, this is a starting point which you can build upon.<br>
<br>
There is a video on how to modify your resume to look more appealing as well as add extra content HERE, if needed (inprogress).<br>
<br>
* Sections with the star will leave space for all items marked with a star (i.e. if you enter something into the first item of the section you will need to enter info into at least all * marked items or<br>
else you will have extra spaces in your resume). These are not required, but as an example, if you enter anything into Job Title and not Dates, you will still get a "( - )" where the dates go. It will not<br>
change anything in job 2 however. If this doesn't make sense, no worries, just make sure to check over your resume when it is done.<br>
